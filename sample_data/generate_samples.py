# Demo-specific assignee pools used *only* on specific Function IDs so the
# bubble map exercises the two edge rendering cases:
#   - CLEAN_ONLY: features in `CLEAN_FIDS` never get defects, so these
#     assignees end up with defect_total = 0 and showcase the
#     marker.sizemin floor that keeps zero-defect bubbles visible.
#   - NO_EXEC_ONLY: features in `NO_EXEC_FIDS` have 実施済 = 0 so
#     incident_rate is NaN for every feature they touch, showcasing the
#     "NaN → 0% fallback with `—` hover value" path.
WBS_CLEAN_ONLY_ASSIGNEES: dict[object, list[str]] = {
    "dev":       ["福田"],
    "test_spec": ["清水"],
    "test_exec": ["松本"],
    None:        ["福田"],
}
WBS_NO_EXEC_ONLY_ASSIGNEES: dict[object, list[str]] = {
    "dev":       ["吉田"],
    "test_spec": ["石川"],
    "test_exec": ["堀"],
    None:        ["吉田"],
}
# Function IDs reserved for the two demo scenarios above. These are excluded
# from the defects generator (CLEAN_FIDS) and get 実施済 = 0 in test counts
# (NO_EXEC_FIDS) respectively.
CLEAN_FIDS   = {"AUDIT01", "ADM001", "SYNC001"}
NO_EXEC_FIDS = {"IMP001", "EXP001"}

# Chance that a sub-task row's 担当者 (N column) is left blank — surfaces as
# 「（未割当）」in the analytics and exercises that fallback path.
WBS_UNASSIGNED_PROB = 0.15


def _write_percent(ws, coord: str, pct: int) -> None:
    """Write an Excel percent-formatted cell: underlying value is fractional
    (0..1) and the display format is ``0%``. This matches how real WBS tools
    store progress columns, so the loader's percent-normalization path is
    exercised by the sample too."""
    cell = ws[coord]
    cell.value = pct / 100.0
    cell.number_format = "0%"


def _fmt_md(d):
    """Format a date as ``MM/DD`` — matches the real WBS convention where
    per-task dates omit the year and the phase anchors on row 6 (J6 / N6)
    carry the absolute 年/月/日 so the year can be reconstructed."""
    if d is None:
        return None
    return f"{d.month:02d}/{d.day:02d}"


def make_wbs() -> Path:
    """Build a WBS that mimics the real format: data starts row 16, Function ID
    appears as '機能ID：XXXX' somewhere in cols E–I, key columns at P/Q/R/S/T/U/V/AA.

    Some Function IDs also carry sub-task breakdown rows: rows *without* a
    Function ID where L column is "●" (fixed marker). In those rows:
      - the column right of the parent's Function ID column holds a task
        label (プログラム開発 / テスト仕様書作成 / テスト実施 / レビューなど);
      - P..V/AA carry the sub-task's own schedule;
      - **N column carries the 担当者** — consumed by the Charts tab's
        担当者×ロール analytics to attribute each role to a person.
    A small fraction of sub-tasks are intentionally left unassigned (empty
    N) so the '（未割当）' fallback is exercised by the sample too.

    For exercising loader behavior the sample also includes a small number
    of intentionally-skipped shapes:
      - A duplicate Function ID row after its first valid occurrence (should
        be skipped by the loader; by default its following ● rows also skip).
      - A row with no Function ID and no ● in L (a noise / spacer row, also
        skipped — exercises the "●-gated" sub-task filter).
    """
    wb = Workbook()
    ws = wb.active
    ws.title = "メイン"

    # Rows 1-15: arbitrary header / metadata noise
    ws["A1"] = "WBS Sample"
    ws["A2"] = "Generated for dashboard4dx demo"
    # Phase anchors (row 6). Real WBS files store the phase start/end here
    # as 年/月/日; per-task dates (Q/R/S/T) are written later as 月/日 only
    # and the loader resolves the year from this window.
    phase_start = date(2026, 1, 1)
    phase_end = date(2026, 5, 31)
    ws["I6"] = "期間"
    ws["J6"] = phase_start.strftime("%Y/%m/%d")
    ws.merge_cells("J6:L6")
    ws["M6"] = "〜"
    ws["N6"] = phase_end.strftime("%Y/%m/%d")
    ws.merge_cells("N6:O6")
    ws["A14"] = "Section"
    ws["E14"] = "Phase / Function"
    ws["L14"] = "●印"
    ws["P14"] = "予定工数"
    ws["Q14"] = "開始予定日"
    ws["R14"] = "終了予定日"
    ws["S14"] = "開始実績日"
    ws["T14"] = "終了実績日"
    ws["U14"] = "投入工数"
    ws["V14"] = "実績進捗率"
    ws["AA14"] = "計画進捗率"

    # ~40% of Function IDs get sub-task breakdowns so both patterns appear.
    # ~70% of Function IDs get sub-task breakdowns — higher than strictly
    # realistic, but the Charts-tab 担当者×ロール analytics needs enough
    # coverage in the demo file to produce a legible heatmap / table.
    with_subs = set(random.sample(UNIQUE_IDS,
                                  k=max(3, int(len(UNIQUE_IDS) * 0.7))))

    today = date(2026, 4, 20)
    row = 16
    for fid in UNIQUE_IDS:
        # Sprinkle the Function ID across one of cols E..I randomly,
        # using one of three accepted formats. Keep sub-col <= J by capping
        # the parent column at H when we know we'll emit sub-tasks.
        cols = ["E", "F", "G", "H"] if fid in with_subs else ["E", "F", "G", "H", "I"]
        target_col = random.choice(cols)
        sub_col = chr(ord(target_col) + 1)  # column immediately right of parent
        fmt = random.choice([f"機能ID：{fid}", f"機能ID:{fid}", fid])
        ws[f"A{row}"] = "Feature"
        ws[f"{target_col}{row}"] = fmt

        planned_effort = round(random.uniform(5, 40), 1)
        actual_effort = round(planned_effort * random.uniform(0.6, 1.3), 1)
        start_plan = today - timedelta(days=random.randint(20, 80))
        end_plan = start_plan + timedelta(days=random.randint(5, 30))
        start_actual = start_plan + timedelta(days=random.randint(-3, 5))
        # 25% of items are still in flight: no actual end date
        end_actual = (end_plan + timedelta(days=random.randint(-3, 10))
                      if random.random() > 0.25 else None)
        actual_pct = (random.randint(70, 100)
                      if end_actual else random.randint(10, 70))
        planned_pct = random.randint(60, 100)

        ws[f"P{row}"] = planned_effort
        ws[f"Q{row}"] = _fmt_md(start_plan)
        ws[f"R{row}"] = _fmt_md(end_plan)
        ws[f"S{row}"] = _fmt_md(start_actual)
        ws[f"T{row}"] = _fmt_md(end_actual)
        ws[f"U{row}"] = actual_effort
        _write_percent(ws, f"V{row}", actual_pct)
        _write_percent(ws, f"AA{row}", planned_pct)
        row += 1

        if fid not in with_subs:
            continue

        # Every feature with sub-tasks gets one row per analytics role so
        # the Charts-tab role views are populated; plus a 50/50 prefix
        # row (設計 or レビュー — no role keyword) for realism. Order is
        # roughly dev → test_spec → test_exec so the schedule slices flow.
        subs: list[tuple[str, object]] = []
        if random.random() < 0.5:
            subs.append(random.choice(
                [p for p in WBS_SUBTASK_POOL if p[1] is None]
            ))
        for role_key in ("dev", "test_spec", "test_exec"):
            subs.append(random.choice(
                [p for p in WBS_SUBTASK_POOL if p[1] == role_key]
            ))

        n_subs = len(subs)
        total_days = max(n_subs, (end_plan - start_plan).days)
        chunk = total_days // n_subs
        remaining_effort = planned_effort
        remaining_actual = actual_effort
        for i, (sub_label, role_key) in enumerate(subs):
            s_start_plan = start_plan + timedelta(days=i * chunk)
            s_end_plan = (end_plan if i == n_subs - 1
                          else start_plan + timedelta(days=(i + 1) * chunk - 1))
            s_start_actual = s_start_plan + timedelta(days=random.randint(-2, 3))
            # Last sub-task inherits the parent's in-flight status roughly
            in_flight = (end_actual is None and i == n_subs - 1) or random.random() < 0.15
            s_end_actual = (None if in_flight
                            else s_end_plan + timedelta(days=random.randint(-2, 4)))
            share = (1.0 / (n_subs - i)) if i < n_subs - 1 else 1.0
            s_planned_effort = round(remaining_effort * share, 1)
            s_actual_effort = round(remaining_actual * share, 1)
            remaining_effort -= s_planned_effort
            remaining_actual -= s_actual_effort

            # Pick 担当者 from the role pool so a name tends to own either
            # dev or test work consistently. Blank ~15% of the time to
            # exercise the (未割当) surface in the analytics.
            #
            # Features flagged as CLEAN_FIDS or NO_EXEC_FIDS route to
            # dedicated assignee pools so those people end up with
            # defect_total = 0 or incident_rate = NaN respectively —
            # exercises the bubble-map edge cases on-screen.
            if random.random() < WBS_UNASSIGNED_PROB:
                assignee = ""
            elif fid in CLEAN_FIDS:
                assignee = random.choice(
                    WBS_CLEAN_ONLY_ASSIGNEES[role_key])
            elif fid in NO_EXEC_FIDS:
                assignee = random.choice(
                    WBS_NO_EXEC_ONLY_ASSIGNEES[role_key])
            else:
                assignee = random.choice(WBS_ROLE_ASSIGNEES[role_key])

            ws[f"A{row}"] = "Task"
            ws[f"L{row}"] = "●"
            ws[f"N{row}"] = assignee
            ws[f"{sub_col}{row}"] = sub_label
            ws[f"P{row}"] = s_planned_effort
            ws[f"Q{row}"] = _fmt_md(s_start_plan)
            ws[f"R{row}"] = _fmt_md(s_end_plan)
            ws[f"S{row}"] = _fmt_md(s_start_actual)
            ws[f"T{row}"] = _fmt_md(s_end_actual)
            ws[f"U{row}"] = s_actual_effort
            _write_percent(ws, f"V{row}",
                           random.randint(70, 100) if s_end_actual
                           else random.randint(10, 70))
            _write_percent(ws, f"AA{row}", random.randint(60, 100))
            row += 1

    # ----- Loader edge-cases ---------------------------------------------------
    # A noise / spacer row with no Function ID and no ● marker. The loader
    # must skip this (sub-task candidacy is gated on L="●").
    ws[f"A{row}"] = "(note)"
    ws[f"F{row}"] = "※ 以下は重複機能IDのテストケース"
    row += 1

    # Duplicate Function ID: AUTH001 appears again well below its first
    # occurrence. The loader keeps only the first; this row and its ● rows
    # below are skipped (default). With the "attach_after_dup" setting ON,
    # the ● rows re-attach to the previously valid parent instead of dying.
    dup_fid = "AUTH001"
    ws[f"A{row}"] = "Feature"
    ws[f"G{row}"] = f"機能ID:{dup_fid}"
    ws[f"P{row}"] = 12.3
    ws[f"Q{row}"] = _fmt_md(date(2026, 4, 15))
    ws[f"R{row}"] = _fmt_md(date(2026, 4, 25))
    ws[f"S{row}"] = _fmt_md(date(2026, 4, 15))
    ws[f"U{row}"] = 8.0
    _write_percent(ws, f"V{row}", 55)
    _write_percent(ws, f"AA{row}", 70)
    row += 1
    for sub in ("追加レビュー", "追加修正"):
        ws[f"A{row}"] = "Task"
        ws[f"L{row}"] = "●"
        ws[f"H{row}"] = sub  # sub_col = G+1 = H
        ws[f"P{row}"] = 6.0
        ws[f"Q{row}"] = _fmt_md(date(2026, 4, 15))
        ws[f"R{row}"] = _fmt_md(date(2026, 4, 20))
        ws[f"S{row}"] = _fmt_md(date(2026, 4, 15))
        ws[f"U{row}"] = 4.0
        _write_percent(ws, f"V{row}", 40)
        _write_percent(ws, f"AA{row}", 60)
        row += 1

    # Save as .xlsm — openpyxl can write the file with a macro-enabled extension
    # even without an actual VBA project; the loader reads it just fine.
    out = OUT_DIR / "wbs.xlsm"
    wb.save(out)
    return out


# ----- 3. Defect tracker CSV --------------------------------------------------
def make_defects() -> Path:
    out = OUT_DIR / "defects.csv"
    trackers = ["不具合管理", "課題管理", "不具合管理", "不具合管理", "タスク"]
    statuses = ["新規", "対応中", "解決", "却下", "再発"]
    # Skewed weights: 実装ミス は少し多め、性能/セキュリティ系はレア
    # — donut + Top-N table がそれっぽく見えるバランスにしておく。
    classes_weighted = [
        ("実装ミス", 6),
        ("仕様漏れ", 4),
        ("設計不備", 4),
        ("環境起因", 3),
        ("再現不可", 3),
        ("性能問題", 2),
        ("セキュリティ起因", 1),
    ]
    classes = [c for c, w in classes_weighted for _ in range(w)]
    assignees = ["alice", "bob", "carol", "dave", "ellen"]

    today = date(2026, 4, 20)
    # CLEAN_FIDS never receive defects — by construction, any assignee
    # who only works on those features ends up with defect_total = 0
    # and demos the marker.sizemin floor in the bubble map.
    defect_eligible_fids = [f for f in UNIQUE_IDS if f not in CLEAN_FIDS]
    rows = []
    for i in range(80):
        fid = random.choice(defect_eligible_fids)
        tracker = random.choice(trackers)
        status = random.choice(statuses)
        start = today - timedelta(days=random.randint(1, 120))
        # 35% have no actual end date (unresolved)
        end = (start + timedelta(days=random.randint(1, 30))
               if random.random() > 0.35 else None)
        # Defect tracker exports dates in MM/DD/YYYY (US-style).
        rows.append({
            "トラッカー": tracker,
            "ステータス": status,
            "担当者": random.choice(assignees),
            "実開始日": start.strftime("%m/%d/%Y"),
            "実終了日": end.strftime("%m/%d/%Y") if end else "",
            "機能ID": fid,
            "問題分類": random.choice(classes),
        })

    # Real exports are usually CP932; we write UTF-8 with BOM here for editor
    # convenience, and the loader transparently handles either encoding.
    with out.open("w", newline="", encoding="utf-8-sig") as f:
        writer = csv.DictWriter(f, fieldnames=list(rows[0].keys()))
        writer.writeheader()
        writer.writerows(rows)
    return out


# ----- 4. Test counts CSV -----------------------------------------------------
def make_test_counts() -> Path:
    """A=機能ID, C=総テスト, D=実施済, E=OK, F=NG (B intentionally blank)."""
    out = OUT_DIR / "test_counts_20260420090000.csv"
    with out.open("w", newline="", encoding="utf-8-sig") as f:
        writer = csv.writer(f)
        writer.writerow(["機能ID", "", "総テスト", "実施済", "OK", "NG"])
        for fid in UNIQUE_IDS:
            total = random.randint(20, 120)
            # NO_EXEC_FIDS: planned but never executed → 実施済 = 0 so
            # incident_rate for that feature is NaN (divide-by-zero). Any
            # WBS assignee whose sub-tasks land only on these features
            # therefore ends up with avg_incident_rate = NaN, which is
            # the second rendering edge case the bubble map now handles.
            if fid in NO_EXEC_FIDS:
                executed = 0
                ok = 0
                ng = 0
            else:
                executed = random.randint(int(total * 0.4), total)
                ok = random.randint(int(executed * 0.6), executed)
                ng = executed - ok
            writer.writerow([fid, "", total, executed, ok, ng])
    return out


