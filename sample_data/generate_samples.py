# ----- 1. Function master xlsx ------------------------------------------------
def make_function_master() -> Path:
    """Build a master xlsx that exercises the loader's tricky cases:
      - Section-header rows (col B filled, col F empty) interspersed in data
      - A trailing 'Total' row (col B filled, col F empty) past the last entry
      - Strike-through formatting on one row (must NOT be excluded)
      - Duplicate Function IDs with different names
    """
    wb = Workbook()
    ws = wb.active
    ws.title = "機能一覧"
    headers = ["No", "Category", "Subsystem", "Owner", "Status",
               "機能ID", "機能名称", "Notes"]
    for col_idx, h in enumerate(headers, start=1):
        ws.cell(row=1, column=col_idx, value=h).font = Font(bold=True)

    # Insert a section header before these IDs (B filled, F empty).
    section_breaks = {"SEARCH01": "--- Search & Discovery ---",
                      "PAY001":   "--- Payment ---"}

    row_cursor = 2
    seq = 1
    for fid, fname in FUNCTION_ENTRIES:
        if fid in section_breaks and not any(
            ws.cell(row=r, column=2).value == section_breaks[fid]
            for r in range(2, row_cursor)
        ):
            # B filled, F empty → loader must skip and continue
            ws.cell(row=row_cursor, column=2, value=section_breaks[fid])
            row_cursor += 1

        ws.cell(row=row_cursor, column=1, value=seq); seq += 1
        ws.cell(row=row_cursor, column=2,
                value=random.choice(["Auth", "User", "Order", "Notify", "Admin"]))
        ws.cell(row=row_cursor, column=3,
                value=random.choice(["Web", "API", "Worker"]))
        ws.cell(row=row_cursor, column=4,
                value=random.choice(["alice", "bob", "carol", "dave"]))
        ws.cell(row=row_cursor, column=5,
                value=random.choice(["Active", "Active", "Active", "Deprecated"]))
        ws.cell(row=row_cursor, column=6, value=fid)
        ws.cell(row=row_cursor, column=7, value=fname)

        if fid == "AUDIT01":
            for col_idx in range(1, 9):
                ws.cell(row=row_cursor, column=col_idx).font = Font(strike=True)
        row_cursor += 1

    # A blank-ish gap, then a trailing 'Total' row (B filled, F empty)
    row_cursor += 2
    ws.cell(row=row_cursor, column=2, value="Total")

    out = OUT_DIR / "function_master.xlsx"
    wb.save(out)
    return out


# ----- 2. WBS xlsm ------------------------------------------------------------
# Sub-task label pool. The keyword-bearing labels are what the role
# analytics section keys on (プログラム開発 / テスト仕様書作成 / テスト実施);
