    ("コードレビュー",                None),
    ("プログラム開発",                "dev"),
    ("プログラム開発（リファクタ）",  "dev"),
    ("テスト仕様書作成",              "test_spec"),
    ("テスト仕様書作成（正常系）",    "test_spec"),
